package D07_practice3;

public class C05_IkiBasamakliSayilariYanYanaYazdirmak {

    public static void main(String[] args) {

        // 2 basamakli sayilari aralarinda bir bosluk olacak sekilde yanyana yazdirin

        System.out.println("Iki basamakli sayilar");
        for (int i = 10; i <100; i++) {
            System.out.print(i+" ");

        }

        int a=10;
        while (a<100){
            System.out.println(a+"");
            a++;
        }
    }
}
